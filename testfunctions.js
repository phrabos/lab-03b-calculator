// these functions are not used in the app, they were 
// created to run tests in example.test.js

export function sumFunction(num1, num2) {
    return num1 + num2;
}

export function subFunction(num1, num2) {
    return num1 - num2;
}

export function multFunction(num1, num2) {
    return num1 * num2;
}

export function divFunction(num1, num2) {
    return num1 / num2;
}